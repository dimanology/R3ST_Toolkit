        col     = bpy.data.collections.get('Collision')
        col_msg = (f"{len(col.objects)} collision object(s)"
                   if col else "no 'Collision' collection")
        self.report({'INFO'},
                    f"Walk mode — '{char.name}'  •  {col_msg}  •  ESC to exit")
        self._redraw(context)

