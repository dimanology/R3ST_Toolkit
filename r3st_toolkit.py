class R3ST_OT_cam_mem_restore(Operator):
    bl_idname      = "r3st.cam_mem_restore"
    bl_label       = "Restore"
    bl_description = "Restore camera rig settings from this memory slot"
    slot: IntProperty(default=1, min=1, max=4, options={'SKIP_SAVE'})

    def execute(self, context):
        pivot = bpy.data.objects.get(PIVOT_NAME)
        if pivot is None:
            self.report({'ERROR'}, "Camera rig not found — create it first.")
            return {'CANCELLED'}
        s = context.scene.r3st_setup
        n = self.slot
        if not getattr(s, f'cam_mem{n}_used', False):
            self.report({'WARNING'}, f"Memory slot {n} is empty — save first.")
            return {'CANCELLED'}
        pivot['yaw']   = getattr(s, f'cam_mem{n}_yaw')
        pivot['pitch'] = getattr(s, f'cam_mem{n}_pitch')
        pivot['dist']  = getattr(s, f'cam_mem{n}_dist')
        pivot['fov']   = getattr(s, f'cam_mem{n}_fov')
        pivot['roll']  = getattr(s, f'cam_mem{n}_roll')
        # Force depsgraph update so drivers recompute immediately
        pivot.update_tag()
        context.view_layer.update()
        self.report({'INFO'}, f"Restored memory slot {n}")
        return {'FINISHED'}


