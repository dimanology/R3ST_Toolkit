def _mat_from_image_path(mat_name, img_path, alpha=1.0):
    """Image-textured emission material.  alpha < 1.0 enables transparency."""
    remove_mat(mat_name)
    img = bpy.data.images.get(os.path.basename(img_path))
    if img is None:
        img = bpy.data.images.load(img_path, check_existing=True)

    mat = bpy.data.materials.new(mat_name)
    mat.use_nodes = True
    N = mat.node_tree.nodes; L = mat.node_tree.links; N.clear()

    out  = N.new('ShaderNodeOutputMaterial'); out.location  = ( 500, 0)
    bsdf = N.new('ShaderNodeBsdfPrincipled'); bsdf.location = ( 100, 0)
    tex  = N.new('ShaderNodeTexImage');       tex.location  = (-200, 0)
    uv   = N.new('ShaderNodeUVMap');          uv.location   = (-450, 0)

    uv.uv_map = 'UVMap'; tex.image = img; tex.extension = 'CLIP'
    # Emission colour carries the texture so lighting has no effect.
    bsdf.inputs['Emission Strength'].default_value = 1.0
    bsdf.inputs['Alpha'].default_value = alpha

    L.new(uv.outputs['UV'],       tex.inputs['Vector'])
    L.new(tex.outputs['Color'],   bsdf.inputs['Emission Color'])
    L.new(bsdf.outputs['BSDF'],   out.inputs['Surface'])

    # Solid-viewport respects diffuse_color alpha when blend_method = BLEND.
    mat.diffuse_color = (1.0, 1.0, 1.0, alpha)
    if alpha < 1.0:
        try:
            mat.blend_method = 'BLEND'          # Blender < 4.2 / EEVEE legacy
        except AttributeError:
            pass
    return mat


def _solid_emit_mat(mat_name, color, alpha=1.0):
    """Flat-colour emission material.  alpha < 1.0 enables transparency."""
    remove_mat(mat_name)
    mat = bpy.data.materials.new(mat_name)
    mat.use_nodes = True
    N = mat.node_tree.nodes; L = mat.node_tree.links; N.clear()

    out  = N.new('ShaderNodeOutputMaterial'); out.location = (400, 0)
    bsdf = N.new('ShaderNodeBsdfPrincipled'); bsdf.location = (0, 0)

    # Drive emission; keep Base Color black so no diffuse shading bleeds in.
    bsdf.inputs['Emission Color'].default_value    = (*color[:3], 1.0)
    bsdf.inputs['Emission Strength'].default_value = 1.0
    bsdf.inputs['Alpha'].default_value             = alpha

    L.new(bsdf.outputs['BSDF'], out.inputs['Surface'])

    mat.diffuse_color = (*color[:3], alpha)
    if alpha < 1.0:
        try:
            mat.blend_method = 'BLEND'
        except AttributeError:
            pass
    return mat


