                _, entry = _find_map_by_name(infos, m)
                if entry:
                    map_id   = entry['id']
                    map_file = f'Map{map_id:03d}.json'
                    map_path = os.path.join(data_dir, map_file)
                    if os.path.exists(map_path):
                        with open(map_path, 'r', encoding='utf-8') as fh:
                            map_data = json.load(fh)
