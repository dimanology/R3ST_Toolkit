def _build_mz3d_note(map_name):
    """
    Scan all scene mesh objects tagged with r3st_map == map_name.
    Objects sharing the same r3st_export_group are collapsed into one entry
    using {group}.{ext} as the model filename.
    Ungrouped objects each get their own entry using {obj.name}.{ext}.
    Returns a <mz3d-tiles> notetag string, or '' if nothing is tagged.
    """
    # grouped: (group, ext) → (sheet, col, row, rg)  — first object wins
    grouped   = {}
    # ungrouped: list of individual entry strings
    ungrouped = []

    for obj in bpy.data.objects:
        if obj.type != 'MESH':
            continue
        if obj.get('r3st_map') != map_name:
            continue
        sheet = obj.get('r3st_sheet', 'B')
        col   = int(obj.get('r3st_col', 1))
        row   = int(obj.get('r3st_row', 0))
        rg    = int(obj.get('r3st_render_group', 1))
        grp   = obj.get('r3st_export_group', '').strip()
        # R3ST_Room is always .obj — never let a stray export_type override that
        if obj.name == ROOM_NAME:
            ext = 'obj'
        else:
            ext = obj.get('r3st_export_type', 'OBJ').lower()

        if grp:
            key = (grp, ext)
            if key not in grouped:
                grouped[key] = (sheet, col, row, rg)
        else:
            model = f'{obj.name}.{ext}'
            ungrouped.append(
                f'{sheet},{col},{row}:model({model}),renderGroup({rg}),climb(false)')

    entries = []
    for (grp, ext), (sheet, col, row, rg) in grouped.items():
        model = f'{grp}.{ext}'
        entries.append(
            f'{sheet},{col},{row}:model({model}),renderGroup({rg}),climb(false)')
    entries.extend(ungrouped)

    if not entries:
        return ''
    return '<mz3d-tiles>\n' + '\n'.join(entries) + '\n</mz3d-tiles>'


# ── Auto-layer constants & helpers ────────────────────────────────────────────

# RPG Maker MZ tile-ID base for each sheet
_SHEET_TILE_BASE = {'A5': 1536, 'B': 0, 'C': 256, 'D': 512, 'E': 768}

def _tile_id(sheet, col, row):
    """Return the RPG Maker MZ tile ID for a given sheet / col / row."""
    base = _SHEET_TILE_BASE.get(sheet, 0)
    return base + (col // 8) * 128 + row * 8 + (col % 8)

# render_group → (sheet, col, row) for auto-tagged objects
# Everything lives on A5 — no reason to use other sheets for anchor tiles.
# A5 layout: Room(0,0)  Collision(1,0)  BG(2,0)  Default(3,0)  FG(4,0)  free(5,0)+
_AUTO_LAYER_SLOTS = {
    0: ('A5', 2, 0),   # Background → A5(2,0) → tile ID 1538
    1: ('A5', 3, 0),   # Default    → A5(3,0) → tile ID 1539
    3: ('A5', 4, 0),   # Foreground → A5(4,0) → tile ID 1540
    # 'COLLISION' handled separately — A5(1,0), always OBJ
}

# render_group key → export bundle name suffix  (full name = "{map_name}_{suffix}")
_AUTO_LAYER_NAMES = {
    0: 'BG',
    1: 'default',
    3: 'FG',
}

# Collision auto-tag: A5 sheet, tile (1,0) — R3ST_Room occupies (0,0)
_COLLISION_SLOT = ('A5', 1, 0)

# render_group → fixed column in the RPG Maker map data array (layer 1, row 0)
_AUTO_TILE_COL = {0: 0, 1: 1, 3: 2}


# ── Layer prefix system ───────────────────────────────────────────────────────
# Prefixes applied to object names when tagging — also determines the export bundle.
# Objects named FG_pillar and FG_arch → bundle "FG" → FG.glb
# Manual variant: rename to FG1_door for a separate foreground bundle.
_LAYER_PREFIXES = {
    '0':         'BG_',
    '1':         'Default_',
    '2':         'CHR_',
    '3':         'FG_',
    'COLLISION': 'Collision_',
}
# Regex that matches any R3ST prefix (including numbered variants: FG1_, BG2_, etc.)
_R3ST_PREFIX_RE = re.compile(r'^(?:BG|FG|Default|Collision|CHR)\d*_')


def _strip_r3st_prefix(name):
    """Remove any [group] bracket prefix AND any R3ST layer prefix from an object name.
    'FG_pillar'        → 'pillar'
    '[Map_01_FG] wall' → 'wall'
    'FG2_door'         → 'door'
    'rock'             → 'rock'
    """
    clean = re.sub(r'^\[.*?\]\s*', '', name)
    return _R3ST_PREFIX_RE.sub('', clean)


def _detect_bundle_prefix(objects):
    """DEPRECATED – kept for compatibility. Use prefix naming instead."""
    prefixes = set()
    for obj in objects:
        clean = _strip_r3st_prefix(obj.name)
        idx   = clean.find('_')
        if idx > 0:
            prefixes.add(clean[:idx])
    return list(prefixes)[0] if len(prefixes) == 1 else None


def _get_scene_bundles():
    """Scan Ortho + Persp collections for tagged objects and return two values:
       - bundles:   dict {group_name: [obj.name, ...]}
       - ungrouped: [obj.name, ...]  (tagged objects with no export_group set)
    """
    bundles   = {}
    ungrouped = []
    for col_name in (COLLECTION_NAME, PERSP_COLLECTION):
        col = bpy.data.collections.get(col_name)
        if not col:
            continue
        for obj in col.objects:
            if obj.type != 'MESH' or 'r3st_map' not in obj:
                continue
            grp = obj.get('r3st_export_group', '').strip()
            if grp:
                bundles.setdefault(grp, []).append(obj.name)
            else:
                ungrouped.append(obj.name)
    return bundles, ungrouped


def _write_anchor_tiles(map_data, map_name):
    """
    Write one anchor tile per auto-tagged render layer into the map data array.
    Position: tile layer 1 (B-E decoration), row 0, col = _AUTO_TILE_COL[rg].
    Only layers that have at least one auto-tagged object on this map are written.
    """
    w    = map_data.get('width',  0)
    h    = map_data.get('height', 0)
    data = map_data.get('data')
    if not data or w <= 0 or h <= 0:
        return

    groups_present = set()
    for obj in bpy.data.objects:
        if obj.type != 'MESH':
            continue
        if obj.get('r3st_map') != map_name:
            continue
        if not obj.get('r3st_auto', False):
            continue
        rg = int(obj.get('r3st_render_group', 1))
        if rg in _AUTO_LAYER_SLOTS:
            groups_present.add(rg)

    # data[layer * w * h + row * w + col]
    for rg in groups_present:
        slot    = _AUTO_LAYER_SLOTS[rg]
        tid     = _tile_id(*slot)
        col     = _AUTO_TILE_COL[rg]
        idx     = 1 * w * h + 0 * w + col
        if idx < len(data):
            data[idx] = tid


# ── Package scanner (for geometry tag dropdown) ───────────────────────────────
# Module-level list prevents Blender's EnumProperty from garbage-collecting items
_pkg_cache = [('NONE', '— no packages found —', '')]


def _scan_packages(project_dir):
    """Return sorted list of R3ST package names found in img/tilesets/."""
    if not project_dir:
        return []
    root   = bpy.path.abspath(project_dir)
    ts_dir = os.path.join(root, 'img', 'tilesets')
    if not os.path.isdir(ts_dir):
        return []
    pkgs = set()
    try:
        for f in os.listdir(ts_dir):
            if f.upper().startswith('R3ST_') and f.lower().endswith('.png'):
                stem  = f[5:-4]                          # strip 'R3ST_' and '.png'
                parts = stem.rsplit('_', 1)              # split off sheet suffix
                if len(parts) == 2 and parts[1] in ('A5', 'B', 'C', 'D', 'E'):
                    pkgs.add(parts[0])
    except Exception:
        pass
    return sorted(pkgs)


def _refresh_pkg_cache(project_dir):
    global _pkg_cache
    pkgs = _scan_packages(project_dir)
    _pkg_cache = [(p, p, '') for p in pkgs] if pkgs else [('NONE', '— no R3ST packages found —', '')]


def _get_pkg_items(self, context):
    if context is not None:
        s = getattr(context.scene, 'r3st_setup', None)
        if s is not None:
            _refresh_pkg_cache(s.project_dir)
    return _pkg_cache


