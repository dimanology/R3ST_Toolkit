# ── XInput (gamepad) support ─────────────────────────────────────────────────
#
# Windows-only, no external dependencies — ctypes calls the XInput DLL directly.
# On non-Windows or if the DLL is unavailable, _poll_xinput() returns None
# silently and walk mode continues with keyboard-only input.

class _XINPUT_GAMEPAD(ctypes.Structure):
    _fields_ = [
        ('wButtons',      ctypes.wintypes.WORD),
        ('bLeftTrigger',  ctypes.wintypes.BYTE),
        ('bRightTrigger', ctypes.wintypes.BYTE),
        ('sThumbLX',      ctypes.wintypes.SHORT),
        ('sThumbLY',      ctypes.wintypes.SHORT),
        ('sThumbRX',      ctypes.wintypes.SHORT),
        ('sThumbRY',      ctypes.wintypes.SHORT),
    ]

class _XINPUT_STATE(ctypes.Structure):
    _fields_ = [
        ('dwPacketNumber', ctypes.wintypes.DWORD),
        ('Gamepad',        _XINPUT_GAMEPAD),
    ]

_XINPUT_DEADZONE_L  = 7849   # left stick deadzone  (Xbox spec)
_XINPUT_TRIGGER_TH  = 30     # trigger threshold 0-255 — below this = noise
_XINPUT_BTN_B       = 0x2000 # B button
_XINPUT_BTN_RB      = 0x0200 # right shoulder (RB)

_xinput_dll    = None   # loaded once on first use
_xinput_failed = False  # True after a failed load — skip retrying


def _xinput_load():
    """Try to load the XInput DLL. Returns the DLL object or None."""
    global _xinput_dll, _xinput_failed
    if _xinput_failed:
        return None
    if _xinput_dll is not None:
        return _xinput_dll
    for name in ('xinput1_4', 'xinput1_3', 'xinput9_1_0'):
        try:
            dll = ctypes.windll.LoadLibrary(name)
            dll.XInputGetState.argtypes = [
                ctypes.wintypes.DWORD,
                ctypes.POINTER(_XINPUT_STATE),
            ]
            dll.XInputGetState.restype = ctypes.wintypes.DWORD
            _xinput_dll = dll
            return dll
        except Exception:
            continue
    _xinput_failed = True
    return None


def _xinput_normalize_stick(x, y, deadzone):
    """Normalize raw stick shorts (-32768..32767) to (-1..1) with circular deadzone."""
    mag = math.sqrt(x * x + y * y)
    if mag < deadzone:
        return 0.0, 0.0
    scale = min((mag - deadzone) / (32767.0 - deadzone), 1.0)
    if mag > 0:
        return (x / mag) * scale, (y / mag) * scale
    return 0.0, 0.0


def _poll_xinput(controller_index=0):
    """
    Poll an XInput controller. Returns a dict with normalized axes and buttons,
    or None if the controller is not connected or XInput is unavailable.

    Keys: lx, ly (left stick -1..1), rt (right trigger 0..1),
          btn_b (bool), btn_rb (bool)
    """
    dll = _xinput_load()
    if dll is None:
        return None
    state = _XINPUT_STATE()
    try:
        result = dll.XInputGetState(controller_index, ctypes.byref(state))
    except Exception:
        return None
    if result != 0:   # 0 = ERROR_SUCCESS
        return None
    g   = state.Gamepad
    lx, ly = _xinput_normalize_stick(g.sThumbLX, g.sThumbLY, _XINPUT_DEADZONE_L)
    rt  = max(0.0, (g.bRightTrigger - _XINPUT_TRIGGER_TH) /
              (255.0 - _XINPUT_TRIGGER_TH)) if g.bRightTrigger > _XINPUT_TRIGGER_TH else 0.0
    return {
        'lx':     lx,
        'ly':     ly,
        'rt':     rt,
        'btn_b':  bool(g.wButtons & _XINPUT_BTN_B),
        'btn_rb': bool(g.wButtons & _XINPUT_BTN_RB),
    }


# ── AABB collision helpers ────────────────────────────────────────────────────

def _get_world_aabb(obj):
    """Return (min_v, max_v) world-space Vector3 for obj's bounding box."""
    corners = [obj.matrix_world @ mathutils.Vector(c) for c in obj.bound_box]
    return (
        mathutils.Vector((min(c.x for c in corners),
                          min(c.y for c in corners),
                          min(c.z for c in corners))),
        mathutils.Vector((max(c.x for c in corners),
                          max(c.y for c in corners),
                          max(c.z for c in corners))),
    )


def _aabbs_overlap(min_a, max_a, min_b, max_b):
    """True if two axis-aligned bounding boxes overlap on all three axes."""
    return (min_a.x <= max_b.x and max_a.x >= min_b.x and
            min_a.y <= max_b.y and max_a.y >= min_b.y and
            min_a.z <= max_b.z and max_a.z >= min_b.z)


def _get_collision_objects():
    """Return all objects in the scene collection named 'Collision'."""
    col = bpy.data.collections.get('Collision')
    return list(col.objects) if col else []


def _walk_would_collide(character, delta):
    """
    Offset the character's AABB by delta and check against all Collision objects.
    Returns True if any overlap is found.
    Call once per axis so that per-axis testing in the timer gives wall-sliding.
    """
    char_min, char_max = _get_world_aabb(character)
    test_min = char_min + delta
    test_max = char_max + delta
    for col_obj in _get_collision_objects():
        if col_obj is character:
            continue
        col_min, col_max = _get_world_aabb(col_obj)
        if _aabbs_overlap(test_min, test_max, col_min, col_max):
            return True
    return False


