def remove_obj(name):
    obj = bpy.data.objects.get(name)
    if obj:
        bpy.data.objects.remove(obj, do_unlink=True)


def remove_mat(name):
    mat = bpy.data.materials.get(name)
    if mat:
        bpy.data.materials.remove(mat)


def get_or_create_collection(name):
    col = bpy.data.collections.get(name)
    if col is None:
        col = bpy.data.collections.new(name)
        bpy.context.scene.collection.children.link(col)
    elif col.name not in [c.name for c in bpy.context.scene.collection.children]:
        bpy.context.scene.collection.children.link(col)
    return col


def link_to_collection(obj, col):
    for c in obj.users_collection:
        c.objects.unlink(obj)
    col.objects.link(obj)


def _ensure_placeholder(name, col, display_type='PLAIN_AXES', display_size=0.5):
    """Create a named Empty in col if an object with that name doesn't exist yet."""
    if bpy.data.objects.get(name):
        return
    emp = bpy.data.objects.new(name, None)
    emp.empty_display_type = display_type
    emp.empty_display_size = display_size
    link_to_collection(emp, col)


def _ensure_scene_collections():
    """
    Create the standard R3ST collection hierarchy if it doesn't already exist,
    and place placeholder Empty objects so the user can see what goes where.
    Called when the camera rig is created — that's the natural scene-setup moment.
    """
    get_or_create_collection(COLLECTION_NAME)           # Ortho  — bg/fg geometry
    get_or_create_collection(CAM_COLLECTION)            # Camera — rig
    persp_col     = get_or_create_collection(PERSP_COLLECTION)   # Persp  — characters
    collision_col = get_or_create_collection('Collision')        # Collision meshes

    # Placeholder: _character (arrows so orientation is clear — walk mode tracks this)
    _ensure_placeholder('_character',  persp_col,     display_type='ARROWS', display_size=0.5)
    # Placeholder: _collision (cube wireframe — shows the expected shape for collision)
    _ensure_placeholder('_collision',  collision_col, display_type='CUBE',   display_size=0.5)


def add_custom_prop(obj, name, value, mn, mx, desc):
    obj[name] = value
    obj.id_properties_ui(name).update(min=mn, max=mx, description=desc, default=value)


def add_driver(target, data_path, index, source, var_specs, expression):
    fc = target.driver_add(data_path, index) if index >= 0 \
         else target.driver_add(data_path)
    d = fc.driver
    d.type = 'SCRIPTED'
    while d.variables:
        d.variables.remove(d.variables[0])
    for vname, vpath in var_specs:
        v = d.variables.new()
        v.name = vname
        v.type = 'SINGLE_PROP'
        v.targets[0].id = source
        v.targets[0].data_path = vpath
    d.expression = expression


def col_letter(col_idx):
    """0 → 'A', 1 → 'B', … 25 → 'Z', 26 → 'AA', etc."""
    s = ''
    n = col_idx
    while True:
        s = _COL_LETTERS[n % 26] + s
        n = n // 26 - 1
        if n < 0:
            break
    return s


def tile_image_name(col_idx, row_idx):
    return f'{col_letter(col_idx)}{row_idx}'


