_r3st_bound_handler = None


def _draw_cam_boundary():
    """
    SpaceView3D POST_VIEW draw callback.
    Draws the camera boundary rectangle on the floor plane (Z ≈ 0) whenever
    cam_bound_enabled is True.  Reads scene properties live so the rectangle
    updates immediately as sliders move.

    Coordinate note
    ───────────────
    RPG Maker:  Y=0 = north (top of map), Y increases south.
    Blender:    Y=0 = south,              Y increases north.
    So the Blender equivalents are:
        X_min = cam_bound_left          X_max = room_w − cam_bound_right
        Y_min = cam_bound_bottom        Y_max = room_d − cam_bound_top
    """
    try:
        scene = bpy.context.scene
        s = scene.r3st_setup
        if not s.cam_bound_enabled:
            return

        r  = scene.r3st_room
        rw = float(r.room_w)
        rd = float(r.room_d)

        x_min = s.cam_bound_left
        x_max = rw - s.cam_bound_right
        y_min = s.cam_bound_bottom        # south / near edge in Blender
        y_max = rd - s.cam_bound_top      # north / far  edge in Blender

        if x_min >= x_max or y_min >= y_max:
            return   # degenerate — skip draw

        z = 0.02   # float slightly above the floor mesh to avoid z-fighting

        # 8 vertices forming a closed rectangle (4 line segments as LINES pairs)
        coords = [
            (x_min, y_min, z), (x_max, y_min, z),   # south edge
            (x_max, y_min, z), (x_max, y_max, z),   # east edge
            (x_max, y_max, z), (x_min, y_max, z),   # north edge
            (x_min, y_max, z), (x_min, y_min, z),   # west edge
        ]

        shader = gpu.shader.from_builtin('UNIFORM_COLOR')
        batch  = batch_for_shader(shader, 'LINES', {'pos': coords})

        gpu.state.blend_set('ALPHA')
        gpu.state.line_width_set(2.5)
        shader.bind()
        shader.uniform_float('color', (0.0, 0.85, 1.0, 0.9))   # bright cyan
        batch.draw(shader)
        gpu.state.line_width_set(1.0)
        gpu.state.blend_set('NONE')

    except Exception:
        pass   # never crash the viewport


# ── Walk Test timer ───────────────────────────────────────────────────────────

def _r3st_walk_timer():
    """
    Recurring timer at 0.05 s (≈20 fps).
    Reads keys_held, computes camera-relative movement, applies per-axis
    AABB collision, moves the character, and syncs the camera pivot XY.
    Returns None to self-unregister when walk test is stopped.
    """
    st = _r3st_walk
    if not st['active']:
        return None   # unregister

    char = bpy.data.objects.get(st['char_name']) if st['char_name'] else None
    if char is None:
        st['active'] = False
        return None

    # Base speed from slider
    speed = 0.1
    try:
        speed = bpy.context.scene.r3st_setup.walk_test_speed
    except Exception:
        pass

    # Poll gamepad early — needed to factor RT into the run decision
    pad           = _poll_xinput()
    st['gamepad'] = pad is not None

    # Stop via gamepad B button (same as ESC)
    if pad and pad['btn_b']:
        st['active'] = False
        return None

    # Run: keyboard Shift OR controller right trigger / RB
    ctrl_running = pad is not None and (pad['rt'] > 0.5 or pad['btn_rb'])
    if st.get('running') or ctrl_running:
        speed = 0.5

    # Camera-relative forward / right vectors derived from pivot yaw
    pivot   = bpy.data.objects.get(PIVOT_NAME)
    yaw_deg = float(pivot.get('yaw', 0.0)) if pivot else 0.0
    yaw_rad = yaw_deg * PI / 180.0
    #
    # Derivation (matches R3ST arm driver):
    #   arm is at  pivot + dist * (sin(yaw), -cos(yaw), ...)
    #   so camera looks FROM arm TOWARD pivot:
    #     forward = (-sin(yaw), +cos(yaw), 0)  — W key pushes into screen
    #     right   = (+cos(yaw), +sin(yaw), 0)  — D key pushes camera-right
    #
    fwd   = mathutils.Vector((-math.sin(yaw_rad),  math.cos(yaw_rad), 0.0))
    right = mathutils.Vector(( math.cos(yaw_rad),  math.sin(yaw_rad), 0.0))
    up    = mathutils.Vector((0.0, 0.0, 1.0))

    # Keyboard input
    keys  = st['keys_held']
    delta = mathutils.Vector((0.0, 0.0, 0.0))
    if 'W' in keys: delta +=  fwd   * speed
    if 'S' in keys: delta -=  fwd   * speed
    if 'D' in keys: delta +=  right * speed
    if 'A' in keys: delta -=  right * speed
    if 'Q' in keys: delta +=  up    * speed
    if 'E' in keys: delta -=  up    * speed

    # Gamepad left stick — ly positive = stick up = forward, lx positive = right
    if pad:
        delta += fwd   * (pad['ly'] * speed)
        delta += right * (pad['lx'] * speed)

    if delta.length_squared > 1e-12:
        # Per-axis AABB test — lets character slide along walls
        dx = mathutils.Vector((delta.x, 0.0, 0.0))
        dy = mathutils.Vector((0.0, delta.y, 0.0))
        dz = mathutils.Vector((0.0, 0.0, delta.z))
        if not _walk_would_collide(char, dx): char.location.x += delta.x
        if not _walk_would_collide(char, dy): char.location.y += delta.y
        if not _walk_would_collide(char, dz): char.location.z += delta.z

        # Camera pivot follows character XY — Z stays fixed (angle preserved)
        if pivot:
            pivot.location.x = char.location.x
            pivot.location.y = char.location.y

            # ── Camera boundary clamping ──────────────────────────────────────
            # Mirrors the RPG Maker smoothclamp borders so the boundary you see
            # in Blender is exactly what plays in-game.
            #
            # Coordinate mapping (RPG Maker Y=0 is north; Blender Y=0 is south):
            #   pivot X: clamp to [left_border,  room_w − right_border]
            #   pivot Y: clamp to [bottom_border, room_d − top_border ]
            try:
                bs = bpy.context.scene.r3st_setup
                if bs.cam_bound_enabled:
                    br = bpy.context.scene.r3st_room
                    rw = float(br.room_w); rd = float(br.room_d)
                    pivot.location.x = max(bs.cam_bound_left,
                                           min(rw - bs.cam_bound_right,
                                               pivot.location.x))
                    pivot.location.y = max(bs.cam_bound_bottom,
                                           min(rd - bs.cam_bound_top,
                                               pivot.location.y))
            except Exception:
                pass

        # The depsgraph handler skips R3ST rig objects to avoid re-rendering on
        # every orbit drag.  But here the pivot genuinely moved (camera panned),
        # so the ortho BG pass must re-render.  Dirty it explicitly.
        if _r3st_continuous['active']:
            _r3st_continuous['dirty_pass1'] = True
            _r3st_continuous['dirty_pass2'] = True
            _r3st_continuous['dirty']       = True

    return 0.05   # reschedule at 20 fps


def _r3st_depsgraph_handler(scene, depsgraph):
    """
    Set per-pass dirty flags when actual scene content changes.

    Orbiting / navigating the viewport moves the R3ST camera rig via its
    drivers, which fires depsgraph_update_post on every mouse move.  We
    deliberately skip updates that are ONLY about the rig objects so that
    pure navigation never triggers a re-render.

    Per-pass tracking: if the changed object lives in the persp collection
    we only dirty pass 2 (chars); otherwise we dirty pass 1 (BG).  When
    both flags are set the timer re-renders both passes; when only one is
    set the other uses its cached numpy array from the previous cycle.
    """
    if not _r3st_continuous['active'] or _r3st_continuous['rendering']:
        return
    st = _r3st_continuous
    # Persp collection — hardcoded name, generated by Create Rig.
    try:
        persp_col = bpy.data.collections.get(PERSP_COLLECTION)
    except Exception:
        persp_col = None

    for update in depsgraph.updates:
        id_data = update.id
        # Skip the R3ST camera rig — drivers fire on every navigation action.
        if isinstance(id_data, bpy.types.Object) and id_data.name in _R3ST_RIG_NAMES:
            continue
        # Skip Scene-level — fires on our own render-state mutations.
        if isinstance(id_data, bpy.types.Scene):
            continue
        if not isinstance(id_data, (bpy.types.Object, bpy.types.Mesh,
                                    bpy.types.Material)):
            continue

        # Determine which pass to dirty based on collection membership.
        if isinstance(id_data, bpy.types.Object) and persp_col is not None:
            if id_data.name in persp_col.all_objects:
                st['dirty_pass2'] = True
            else:
                st['dirty_pass1'] = True
        else:
            # Mesh / Material — can't cheaply determine pass; dirty both.
            st['dirty_pass1'] = True
            st['dirty_pass2'] = True

        st['dirty'] = True
        return


def _r3st_continuous_timer():
    """
    Recurring timer (0.3 s).  Triggers a preview render when the scene has
    changed and the debounce interval has elapsed.
    Returns None to unregister itself once continuous mode is stopped.
    """
    import time
    st = _r3st_continuous
    if not st['active']:
        return None     # stops the timer
    # During walk test the pivot moves every 0.05 s — drop the debounce to
    # near-zero so the ortho BG re-renders each tick and the pan feels live.
    # Normal editing keeps the full debounce to avoid render spam.
    effective_debounce = 0.05 if _r3st_walk['active'] else st['debounce']
    if st['dirty'] and (time.time() - st['last_time']) >= effective_debounce:
        st['dirty']     = False
        st['last_time'] = time.time()
        st['rendering'] = True
        try:
            bpy.ops.r3st.render_preview(is_continuous=True)
        except Exception:
            pass
        finally:
            st['rendering'] = False
    return 0.3          # reschedule


# ── Operator: Continuous Render Toggle ───────────────────────────────────────

class R3ST_OT_continuous_render(Operator):
    bl_idname      = "r3st.continuous_render"
    bl_label       = "Continuous Render"
    bl_description = (
        "Toggle live preview: re-renders automatically after every scene "
        "change (uses viewport OpenGL for speed). Click again to stop."
    )

    def execute(self, context):
        import time
        st = _r3st_continuous

        if st['active']:
            # ── Stop ──────────────────────────────────────────────────────────
            st['active'] = False
            if _r3st_depsgraph_handler in bpy.app.handlers.depsgraph_update_post:
                bpy.app.handlers.depsgraph_update_post.remove(
                    _r3st_depsgraph_handler)
            self.report({'INFO'}, "R3ST: continuous render stopped.")
        else:
            # ── Start ─────────────────────────────────────────────────────────
            st['active']      = True
            st['dirty']       = False
            st['dirty_pass1'] = True   # force full render on first activation
            st['dirty_pass2'] = True
            st['last_time']   = time.time()
            _r3st_pass_cache.clear()   # discard stale cached passes
            if _r3st_depsgraph_handler not in bpy.app.handlers.depsgraph_update_post:
                bpy.app.handlers.depsgraph_update_post.append(
                    _r3st_depsgraph_handler)
            if not bpy.app.timers.is_registered(_r3st_continuous_timer):
                bpy.app.timers.register(_r3st_continuous_timer,
                                        first_interval=0.3)
            self.report({'INFO'}, "R3ST: continuous render started.")

        # Force panel redraw so the button label/icon updates immediately
        for window in context.window_manager.windows:
            for area in window.screen.areas:
                if area.type == 'VIEW_3D':
                    area.tag_redraw()

        return {'FINISHED'}


